package Sem_2_Data_and_functions;

import java.util.Arrays;
import java.util.TreeMap;

public class Task_2 {

    static void main() {
        // Создание и инициализация целочисленного массива с произвольными не повторяющимися числами
        int[] arr = { 1, 3, 2, 6, 9, 10, 1, 2, 5, 4, 9, 6, 20 };

        // Вызов метода сортировки, преобразование полученного массива в строку и вывод в консоль
        System.out.println(Arrays.toString(countSort(arr)));
    }

    /**
     * Метод для сортировки массива методом подсчета с использованием TreeMap
     * @param arr исходный неотсортированный массив
     * @return новый отсортированный массив
     */
    public static int[] countSort(int[] arr) {

        // Создание нового пустого массива такой же длины, как исходный, для хранения результата
        int[] tmpArr = new int[arr.length];

        // Объявление и создание объекта TreeMap. Ключ — само число, значение — сколько раз оно встретилось.
        // TreeMap гарантирует, что все ключи (числа) будут автоматически расставлены по возрастанию.
        TreeMap<Integer, Integer> map = new TreeMap<>();

        // 1. Цикл "for-each" проходит по каждому элементу (value) исходного массива
        for (int value : arr) {
            // Пытаемся получить из карты текущее количество вхождений числа. Если числа еще нет — возвращаем 0.
            int count = map.getOrDefault(value, 0);
            // Записываем в карту число и увеличиваем счетчик его вхождений на 1
            map.put(value, count + 1);
        }

        // Переменная-счетчик (индекс) для заполнения результирующего массива
        int i = 0;

        // 2. Внешний цикл проходит по всем ключам (уникальным числам) в TreeMap (они уже идут по порядку)
        for (Integer key : map.keySet()) {
            // Внутренний цикл выполняется столько раз, сколько конкретное число (key) встретилось в исходном массиве
            for (int j = 0; j < map.get(key); j++) {
                // Записываем число в результирующий массив и увеличиваем индекс на единицу для следующей записи
                tmpArr[i++] = key;
            }
        }

        // Возврат полностью заполненного и отсортированного массива
        return tmpArr;
    }
}